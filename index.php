<p>The topics and the presenters are diverse, but will appeal to anyone who has an interest in technology and the internet. This is an event for web designers, developers, marketers, copy writers, SEOs, sys admins and anyone who operates a business with a web presence to get together and share their knowledge. The endearing quality of the unconference format is that you have options all day. If the presentation you choose isn’t what you were expecting, you can always jump over into another room and check out something else.</p>

